package com.ww.springboot.boot.model;

public class ExportMessageDto {
	private String existRow;
    private String errorRow;
    private String remark;
    
    
	public String getExistRow() {
		return existRow;
	}
	public void setExistRow(String existRow) {
		this.existRow = existRow;
	}
	public String getErrorRow() {
		return errorRow;
	}
	public void setErrorRow(String errorRow) {
		this.errorRow = errorRow;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
    
    

}

